package com.comcast.xconf.utils.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

@Retention(RetentionPolicy.RUNTIME)
@Target({ElementType.METHOD, ElementType.TYPE})
public @interface Migration {
    Class oldKey() default Object.class;
    Class newKey() default Object.class;
    Class oldEntity() default Object.class;
    Class newEntity() default Object.class;
    String migrationURL() default "";
}
